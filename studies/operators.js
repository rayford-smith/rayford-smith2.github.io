/**
 * OPERATORS
 * 
 * they do stuff to variables
 */


// 1. Assignment Operators // 
// for assigning values to variables
let val = 1;
val += 2; // val = val + 2 // val = 3
val -= 1; // val = val - 1 // val = 2
val *= 3; // val = val * 3 // val = 6
val /= 2; // val = val / 2 // val = 3
val %= 3; // val = val % 3 // val = 0


// 2. Arithmetic Operators //
// for performing arithmetic on numbers
val = 5 + 10; // val = 15
val = 12 - 6; // val = 6
val = 3 * 4; // val = 12
val = 100 / 20; // val = 5
val = 12 % 5; // val = 2
val++; // val = val + 1 // val = 3
val--; // val = val - 1 // val = 2


// 3. Comparison Operators // 
// for determining equality between values
val = 5 == 5; // val = true
val = 5 === "5"; // val = false
val = 5 != 6; // val = true
val = 5 !== "5" // val = true
val = 5 > 6 // val = false
val = 5 < 6 // val = true


// 4. Logical Operators //
// for determining the logic between variables or values
val = (5 == 5 && 6 == 7) // val = false
val = (5 == 5 || 6 == 7) // val = true
val = !(6 == 7) // val = true


// 5. Ternary Operator //
// for assigning a value to a variable based on some condition
val = (5 == 5) ? true:false; // val = true 
val = (5 == 6) ? true:false; // val = false 